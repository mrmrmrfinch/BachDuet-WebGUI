      self.$refs.usersKeyboardUIref.toggleAttack(name);
    });

    keyboard.up(function (note) {
      let name = Midi.midiToNoteName(note.note, { sharps: true });
      self.$refs.usersKeyboardUIref.toggleRelease(name);
    });
